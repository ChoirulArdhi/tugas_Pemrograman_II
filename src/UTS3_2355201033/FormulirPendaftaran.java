    private boolean isValidEmail(String email) {
    return email.matches("^[\\w-\\.]+@([\\w-]+\\.)+[\\w-]{2,4}$");
}
   private void toggleTheme(boolean isDark) {
    Color bgColor = isDark ? Color.DARK_GRAY : Color.WHITE;
    Color fgColor = isDark ? Color.WHITE : Color.BLACK;

    getContentPane().setBackground(bgColor);
    NamaLabel.setForeground(fgColor);
    jLabel2.setForeground(fgColor);
    AlamatLabel.setForeground(fgColor);
    AsalLabel.setForeground(fgColor);
    jLabel4.setForeground(fgColor);
    outputArea.setBackground(isDark ? Color.BLACK : Color.WHITE);
    outputArea.setForeground(fgColor);
}
 
