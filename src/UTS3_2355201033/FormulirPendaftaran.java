    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String email = emailField.getText();
        String Nama = NamaField.getText();
        String Alamat = AlamatField.getText();
        String Asal = AsalField.getText();
        String gender = (String) genderComboBox.getSelectedItem();

        if (email.isEmpty() || Nama.isEmpty() || Alamat.isEmpty() || Asal.isEmpty()) {
            outputArea.setText("Semua kolom harus diisi!");
        } else if (!isValidEmail(email)) {
        outputArea.setText("Format email anda salah!");}
        else {
            outputArea.setText(String.format(String.format(
                "=== DETAIL INFORMASI ===\n" +
                "Nama         : %s\n" +
                "Alamat       : %s\n" +
                "Email        : %s\n" +
                "Gender       : %s\n" +
                "Asal Sekolah : %s\n" +
                "Status  : Registrasi Berhasil!\n", 
                Nama, Alamat, email, gender, Asal
            )));
        }
        setTitle("Formulir Pendaftaran Sederhana");

    }//GEN-LAST:event_jButton1ActionPerformed

